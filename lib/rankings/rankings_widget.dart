                          Row(
                            mainAxisSize: MainAxisSize.max,
                            children: [
                              Text(
                                'Your Country Ranking: ',
                                style: FlutterFlowTheme.of(context).subtitle1,
                              ),
                            ],
                          ),
                        ],
                      ),
                      Column(
                        mainAxisSize: MainAxisSize.max,
                        children: [
                          Row(
                            mainAxisSize: MainAxisSize.max,
                            children: [
                              Text(
                                'Your World Ranking: ',
                                style: FlutterFlowTheme.of(context).subtitle1,
                              ),
                            ],
